function printName1() {
	  name = document.getElementById('name1').value;      
	  document.getElementById('result1').value = name;      
	  orderNum = document.getElementById('quantity1').value;      
	  document.getElementById('resultNum1').value = orderNum;
	  orderPrice = document.getElementById('price1').value;      
	  document.getElementById('resultPrice1').value = orderPrice;
	  if(document.getElementById('quantity1') !=1)
	  document.getElementById('resultPrice1').value = document.getElementById('resultNum1').value*orderPrice;
   }
   

